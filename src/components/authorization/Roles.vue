      ></el-pagination>-->
    </el-card>

    <set-rights-dialog
      ref="setRightsDialogRef"
      :rightsTreeList="rightsList"
      :defaultKeys="childrenKeys"
      :roleId="roleId"
      @resetDefaultKeys="resetDefaultKeys"
      @refreshRolesList="getRolesList"
    ></set-rights-dialog>
  </div>
</template>

<script>
import SetRightsDialog from './setRightsDialog'

export default {
  components: { SetRightsDialog },
  data() {
    return {
      rolesList: [],
      rightsList: {},
      childrenKeys: [],
      roleId: ''
    }
  },
  created() {
    this.getRolesList()
  },
  methods: {
    async getRolesList() {
      const { data: res } = await this.$http.get('roles ')
      if (res.meta.status !== 200) {
        return this.$message.error('获取角色列表失败')
      }
      this.rolesList = res.data
    },
    async deleteRight(role, rightId) {
      const confirmResult = await this.$confirm(
        '此操作将永久删除该权限，是否继续？',
        '提示',
        {
          confirmButtonText: '确定',
          cancelButtonText: '取消',
          type: 'warning'
        }
      ).catch(err => err)
      if (confirmResult !== 'confirm') {
        return this.$message.error('已经取消删除')
      }
      const { data: res } = await this.$http.delete(
        `roles/${role.id}/rights/${rightId}`
      )
      if (res.meta.status !== 200) {
        return this.$message.error('删除权限失败')
      }
      role.children = res.data
    },
    async showSetRightsDialog(role) {
      this.roleId = (role.id).toString()
      console.log(this.roleId)
      const { data: res } = await this.$http.get('rights/tree')
      if (res.meta.status !== 200) {
        return this.$message.error('获取权限列表失败')
      }
      this.rightsList = res.data
      this.getLeafKeys(role, this.childrenKeys)
      this.$refs.setRightsDialogRef.setRightsDialogVisible = true
    },
    getLeafKeys(node, arr) {
      if (!node.children) {
        return arr.push(node.id)
      }
      node.children.forEach(item => this.getLeafKeys(item, arr))
    },
    resetDefaultKeys() {
      this.childrenKeys = []
    }
  }
}
</script>

